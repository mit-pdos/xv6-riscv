int set_priority(int priority, int pid) {
  struct proc *p;
  if (priority < 0 || priority > 100) {
    return -1;
  }
  for(p = proc; p < &proc[NPROC]; p++) {
    acquire(&p->lock);
    if(p->pid == pid) {
      int old_sp = p->static_priority;

      int niceness;
      if(p->stime + p->running_time == 0) niceness = 5;
      else niceness = (p->stime * 10) / (p->stime + p->running_time);
      int d_priority = max(0, min(p->static_priority - niceness + 5, 100));

      p->static_priority = priority;
      p->running_time = 0;
      p->stime = 0;
      release(&p->lock);

      if(priority < d_priority) {
        yield();
      }

      return old_sp;
    }
    release(&p->lock);
  }
  return -1;
}

// Wait for a child process to exit and return its pid.
// Return -1 if this process has no children.
int
waitx(uint64 addr, uint* wtime, uint* rtime)
{
  struct proc *np;
  int havekids, pid;
  struct proc *p = myproc();

  acquire(&wait_lock);

  for(;;){
    // Scan through table looking for exited children.
    havekids = 0;
    for(np = proc; np < &proc[NPROC]; np++){
      if(np->parent == p){
        // make sure the child isn't still in exit() or swtch().
        acquire(&np->lock);

        havekids = 1;
        if(np->state == ZOMBIE){
          // Found one.
          pid = np->pid;
          *rtime = np->rtime;
          *wtime = np->etime - np->ctime - np->rtime;
          if(addr != 0 && copyout(p->pagetable, addr, (char *)&np->xstate,
                                  sizeof(np->xstate)) < 0) {
            release(&np->lock);
            release(&wait_lock);
            return -1;
          }
          freeproc(np);
          release(&np->lock);
          release(&wait_lock);
          return pid;
        }
        release(&np->lock);
      }
    }

    // No point waiting if we don't have any children.
    if(!havekids || p->killed){
      release(&wait_lock);
      return -1;
    }

    // Wait for a child to exit.
    sleep(p, &wait_lock);  //DOC: wait-sleep
  }
}

void
update_time()
{
  struct proc* p;
  for (p = proc; p < &proc[NPROC]; p++) {
    acquire(&p->lock);
    if (p->state == RUNNING) {
      p->rtime++;
      p->running_time++;
    } else if (p->state == SLEEPING) {
      p->stime++;
    } else if(p->state == RUNNABLE) {
      p->wait_time++;
      if(p->wait_time >= 20) {
        if(p->queue > 0) {
          p->wait_time = 0;
          pop(p->queue);
          p->queue--;
          push(p->queue, p);
        }
      }
    }
    release(&p->lock); 
  }
  #ifdef MLFQ
  for(p = proc; p < &proc[NPROC]; p++) {
    acquire(&p->lock);
    if(p->pid > 3) 
      printf("%d %d %d\n", ticks, p->pid, p->queue);
    release(&p->lock);
  }
  #endif
}


void init_mlfq() {
  // printf("init mlfq\n");
    for (int i = 0; i < 5; i++) {
        mlfq_pointers[i][0] = -1;
        mlfq_pointers[i][1] = -1;
    }
}

void push(int mlfq_num, struct proc *p) {
    // printf("pushing %d to mlfq %d\n", p->pid, mlfq_num);
    if (mlfq_pointers[mlfq_num][0] == -1) {
        mlfq_pointers[mlfq_num][0] = 0;
        mlfq_pointers[mlfq_num][1] = 1;
        mlfq[mlfq_num][0] = p;
    } else {
        mlfq[mlfq_num][mlfq_pointers[mlfq_num][1]] = p;
        mlfq_pointers[mlfq_num][1] = (mlfq_pointers[mlfq_num][1] + 1) % NPROC;
    }
    // print_mlfq();
}

struct proc *pop(int mlfq_num) {
    // printf("popping from mlfq %d", mlfq_num);
    if (mlfq_pointers[mlfq_num][0] == -1) {
        return 0;
    } else {
        struct proc *p = mlfq[mlfq_num][mlfq_pointers[mlfq_num][0]];
        mlfq_pointers[mlfq_num][0] = (mlfq_pointers[mlfq_num][0] + 1) % NPROC;
        if (mlfq_pointers[mlfq_num][0] == mlfq_pointers[mlfq_num][1]) {
            mlfq_pointers[mlfq_num][0] = -1;
            mlfq_pointers[mlfq_num][1] = -1;
        }
        return p;
    }
}

int is_empty(int mlfq_num) {
    return mlfq_pointers[mlfq_num][0] == -1;
}

struct proc* front(int mlfq_num) {
    if (mlfq_pointers[mlfq_num][0] == -1) {
        return 0;
    } else {
        return mlfq[mlfq_num][mlfq_pointers[mlfq_num][0]];
    }
}

struct proc* highest() {
    for (int i = 0; i < 5; i++) {
        if (!is_empty(i)) {
            // printf("highest: %d", front(i)->pid);
            return front(i);
        }
    }
    return 0;
}

void print_mlfq() {
    for (int i = 0; i < 5; i++) {
        printf("MLFQ %d: ", i);
        for (int j = mlfq_pointers[i][0]; j != mlfq_pointers[i][1]; j = (j + 1) % NPROC) {
            printf("%d ", mlfq[i][j]->pid);
        }
        printf("\n");
    }
}

int sigalarm(int ticks, void (*handler)()) {
  struct proc *p = myproc();
  if (ticks < 0) {
    return -1;
  }
  p->alarm_interval = ticks;
  p->alarm_handler = handler;
  p->alarm_flag = 1;
  p->alarm_time = 0;
  return 0;
}

int sigreturn(void) {
  struct proc *p = myproc();
  *p->trapframe = p->old_trapframe;
  p->alarm_time = 0;
  p->alarm_flag = 1;
  return p->old_trapframe.a0;
}
